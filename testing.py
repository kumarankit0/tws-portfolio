# git commands
# git status: shows the current status of the repository, including changes that have been staged, changes that have not been staged, and untracked files.
# git add: stages changes for the next commit.
# git commit: creates a new commit with the staged changes.
# git push: pushes the committed changes to a remote repository.
# git restore: restores files in the working directory to their state in the last commit.